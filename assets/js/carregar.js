document.querySelector('#pesquisa').addEventListener('keypress', function (e) {
    if (e.key === 'Enter') {
        const pesquisa = document.querySelector("#pesquisa").value

        for(obj in viagens) {
            if(viagens[obj].pais === pesquisa || viagens[obj].estado === pesquisa){
                alert("A viagem para " + viagens[obj].estado + " - " + viagens[obj].pais + " esta agendada para iniciar em " + viagens[obj].dataInicio + ". O rio que será tratado é o " + viagens[obj].rio + "!");
            }
        }
    }
});

